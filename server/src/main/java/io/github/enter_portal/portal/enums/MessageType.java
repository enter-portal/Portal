package io.github.enter_portal.portal.enums;

/** Enum representing the different types of messages that can be sent or received. */
public enum MessageType {
    /** A text-based message. */
    TEXT,

    /** An audio message. */
    AUDIO,

    /** An image message. */
    IMAGE,

    /** A video message. */
    VIDEO,

    /** A generic file message. */
    FILE
}
